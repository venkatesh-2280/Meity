package cdfi.fintantra.meity;

public class pojoAttachment {
    String doctype;
    String subtype;
    String filensme;
    String remarks;
    String attachgid;

    public String getDoctype() {
        return doctype;
    }

    public void setDoctype(String doctype) {
        this.doctype = doctype;
    }

    public String getSubtype() {
        return subtype;
    }

    public void setSubtype(String subtype) {
        this.subtype = subtype;
    }

    public String getFilensme() {
        return filensme;
    }

    public void setFilensme(String filensme) {
        this.filensme = filensme;
    }

    public String getRemarks() {
        return remarks;
    }

    public void setRemarks(String remarks) {
        this.remarks = remarks;
    }

    public String getAttachgid() {
        return attachgid;
    }

    public void setAttachgid(String attachgid) {
        this.attachgid = attachgid;
    }
}
